package Arithmetic_Test;

public class Arithmetic {
  static int add(int a, int b) {
    return a + b;
  }

  static int substraction(int a, int b) {
    return a - b;
  }

  static int multiplication(int a, int b) {
    return a * b;
  }

  static int division(int a, int b) {
    return a / b;
  }
}
